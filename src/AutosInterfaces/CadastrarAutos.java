    private void CadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CadastrarActionPerformed
             if(vTxfield(Marca) ||  vTxfield(codigo) || vTxfield(modelo) || vTxfield(placa)){
            JOptionPane.showMessageDialog(this,"Favor preencher os campos!");
        }
        else{
        Veiculo veiculo = new Veiculo();
        veiculo.setCodVeiculo(Integer.parseInt(codigo.getText()));
        veiculo.setMarca(Marca.getText());
        veiculo.setModelo(modelo.getText());
        veiculo.setPlaca(placa.getText());
        
        
        try {
            BufferedWriter gravando = new BufferedWriter(
            new FileWriter("C:\\Dados\\veiculos.txt" ,true));
            gravando.write(
                     veiculo.getCodVeiculo()+";"
                    +veiculo.getMarca()+";"
                    +veiculo.getModelo()+";"
                    +veiculo.getPlaca()+"\r\n");
            gravando.close();
            
            JOptionPane.showMessageDialog(this, "Usuario "+ veiculo.getModelo()+" registrado com sucesso!");
            codigo.setText(" ");
            Marca.setText(" ");
            modelo.setText(" ");
            placa.setText(" ");
            codigo.requestFocus();
            
        }catch(IOException e){
            JOptionPane.showMessageDialog(this, "Opa alguma coisa nao deu certo! "+e.getMessage());
        }
      }
        
    }//GEN-LAST:event_CadastrarActionPerformed

